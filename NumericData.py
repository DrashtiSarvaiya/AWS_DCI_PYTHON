
print("Python has three numeric types: int, float, and complex")

# This is for Integer Data-type
myValue=1 #defines a variable

print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue))) #String==str()

#This is for Float Data-type
myValue=3.14
print(myValue)
print(type(myValue)) #type() function to know the data type
print(str(myValue) + " is of the data type " + str(type(myValue)))
print(int(myValue)) # This will gives output in a integer form rather than decimal eg:3
print(float(myValue)) # This will gives output in a decimal form rather than integer eg:3.14
print(str(myValue) + " is of the data type " + str(type(myValue)))

myValue=5j #Complex Data-type
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

myValue=True #Boolean Datatype (1=True and 0=False)
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

myValue=False #Boolean Datatype (1=True and 0=False)
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))